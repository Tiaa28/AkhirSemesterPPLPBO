    private void kursiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kursiActionPerformed
        String result = kursi.getSelectedItem().toString();
        
        switch(result) {
            case "Pilih": 
                buttonGroup1.clearSelection();
                hargaSatuan.setText("");
                break;
            case "A": 
                reguler=50000;
                ultra=70000;
                gold=90000;
                break;
            case "B": 
                reguler=45000;
                ultra=65000;
                gold=85000;
                break;
            case "C": 
                reguler=40000;
                ultra=60000;
                gold=80000;
                break;
            case "D": 
                reguler=40000;
                ultra=60000;
                gold=80000;
                break;
            default:
                JOptionPane.showMessageDialog(rootPane, "Kursi tidak valid");
                break;
        }
    }//GEN-LAST:event_kursiActionPerformed

