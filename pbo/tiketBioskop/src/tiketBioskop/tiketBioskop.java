    private void jumlahBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jumlahBeliActionPerformed
        beli = Integer.parseInt(jumlahBeli.getText());
        harga = Integer.parseInt(hargaSatuan.getText());
        total = harga * beli;
        totalBayar.setText(String.valueOf(total));
    }//GEN-LAST:event_jumlahBeliActionPerformed

    private void jumlahBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jumlahBayarActionPerformed
        beli = Integer.parseInt(jumlahBayar.getText());
        kembali = Integer.parseInt(totalBayar.getText());
        total = beli - kembali;
        kembalian.setText(String.valueOf(total));
    }//GEN-LAST:event_jumlahBayarActionPerformed

    private void jumlahBeliKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jumlahBeliKeyReleased
        int a,b,c;
        a = Integer.parseInt(jumlahBeli.getText());
        b = Integer.parseInt(hargaSatuan.getText());
        c = a * b;
        totalBayar.setText(String.valueOf(""+c));
    }//GEN-LAST:event_jumlahBeliKeyReleased

